#include <iostream>

int main(){
    int numberDecimal = 15;
    int numberOctal = 017;
    int numberHex = 0x0f;
    int numberBin = 0b00001111;

    std::cout << "Number Decimal: " << numberDecimal << std::endl;
    std::cout << "Number Octal: " << numberOctal << std::endl;
    std::cout << "Number Hex: " << numberHex << std::endl;
    std::cout << "Number Binary: " << numberBin << std::endl;
    

    return 0;
}
